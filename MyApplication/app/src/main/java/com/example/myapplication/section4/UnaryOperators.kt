package com.example.myapplication.section4

fun main(args: Array<String>) {

    var num:Double = 25.5
    var isCheck:Boolean = true

    println("+num = ${+num}")
    println("-num = ${-num}")
    println("++num = ${++num}")
    println("--num = ${--num}")
    println("!isCheck = ${!isCheck}")


    println("-----------------")
    var res:Double = 4.7
    println("res = $res")
    println("res++ = ${res++}")
    println("res = $res")
    println("++res = ${++res}")
    println("res = $res")

}