package com.example.myapplication

fun main (args:Array<String>){
    var a:Boolean = true
    var b:Char = 'A'
    var c:Byte = 10
    var d:Short = -356
    var e:Int = 1000
    var f:Long = -4593939393L
    var g:Float = 11.6f
    var h:Double = 11.6

    println("Boolean: $a")
    println("Char: $b")
    println("Byte: $c")
    println("Short: $d")
    println("Int: $e")
    println("Long: $f")
    println("Float: $g")
    println("Double: $h")
}